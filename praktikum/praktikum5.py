# List berisi tiga pasangan titik
titik_list = [(0, 0), (50, 50), (100, 0)]

# Menampilkan semua titik
print("Daftar titik dalam list:")
for titik in titik_list:
    print(titik)

# Tuple untuk titik pusat
pusat = (0, 0)

print(f"Titik pusat berada di {pusat}")

# Dictionary berisi atribut objek
objek = {"x": 10, "y": 20, "warna": "biru"}

print(f"Titik ({objek['x']}, {objek['y']}) berwarna {objek['warna']}")